    public void getGradeHistorico() throws ParseException{
       //Consultar historicos
        Historicos oHistorico = new Historicos();

        ResultSet resultSet = null;

        String[] colunasTabela = new String[]{"#ID", "Data", "Usuario", "Tipo de registro"};
        DefaultTableModel modeloTabela = new DefaultTableModel(null, colunasTabela) {

            @Override
            public boolean isCellEditable(int row, int col) {
                return false;
            }
        };
        gradeHistoricojTable.setModel(modeloTabela);
        gradeHistoricojTable.getColumnModel().getColumn(0).setPreferredWidth(5);
        gradeHistoricojTable.getColumnModel().getColumn(1).setPreferredWidth(80);
        gradeHistoricojTable.getColumnModel().getColumn(2).setPreferredWidth(400);
        gradeHistoricojTable.getColumnModel().getColumn(3).setPreferredWidth(150);

        gradeHistoricojTable.setDefaultRenderer(Object.class, new ColorRenderer()); // DEIXA A GRADE ZEBRADA

try {
            resultSet = oHistorico.getConsultar();
            if (resultSet.getRow() == 1){
             resultSet.first();
            }


        } catch (SQLException ex) {
            Logger.getLogger(Historicos.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {

            while (resultSet.next()) {

                //Fim formata√ß√£o

                modeloTabela.addRow(new String[]{
                    Funcoes.getCodigoFormat(resultSet.getInt("COD_HISTORICO")),
                    resultSet.getString("DATA"),
                    resultSet.getString("PESSOA"),
                    resultSet.getString("TIPO_HISTORICO"),

                });

            }

        } catch (SQLException ex) {
            Logger.getLogger(Historicos.class.getName()).log(Level.SEVERE, null, ex);
        }

           }

       public void getFieldHistorico(int cod_historico) throws ParseException{

        Historicos oHistorico = new Historicos();
        ResultSet rs = null;
        oHistorico.setCod_historico(cod_historico);

        try {
            rs = oHistorico.getConsultar();
            rs.next();

            //Pegar campos
            idCodHistoricojSpinner.setValue(rs.getInt("COD_HISTORICO"));
            pessoajTextField.setText(rs.getString("PESSOA"));
            datajTextField.setText(rs.getString("DATA"));
            tipoHistoricojTextField.setText(rs.getString("TIPO_HISTORICO"));
            historicojTextArea.setText(rs.getString("HISTORICO"));

        } catch (SQLException ex) {
            Logger.getLogger(Historicos.class.getName()).log(Level.SEVERE, null, ex);
        }



    }


